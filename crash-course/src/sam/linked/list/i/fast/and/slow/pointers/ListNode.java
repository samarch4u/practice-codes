package sam.linked.list.i.fast.and.slow.pointers;

public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int x) {
        val = x;
        next = null;
    }

    public int getVal() {
        return val;
    }

    public ListNode getNext() {
        return next;
    }
}
