package sam.linked_list.i_fast_and_slow_pointers;

public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int x) {
        val = x;
        next = null;
    }

    public int getVal() {
        return val;
    }

    public ListNode getNext() {
        return next;
    }
}
